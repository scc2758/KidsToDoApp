package com.example.kidstodoapp.pkg;

public class Trophy {

        private String name;
        int points;

        Trophy(String name, int points) {
            this.name = name;
            this.points = points;
        }


        public String getName() {
            return name;
        }

        public int getPoints() {
            return points;
        }

        public void setName(String name) {
            this.name = name;
        }

        public void setPoints(int points) {
            this.name = name;
        }

}
